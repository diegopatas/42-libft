#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (ft_isalpha(c))
	{
		while (*s || *s == '\0')
		{
			if (*s == c)
				return ((char *)s);
			s++;
		}
	}
	return (0);
}

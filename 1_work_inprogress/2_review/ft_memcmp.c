#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, t_size n)
{
	while ((*(char *)s1 == *(char *)s2) && (n - 1 > 0))
	{
		s1++;
		s2++;
		n--;
	}
	return (*(char *)s1 - *(char *)s2);
}

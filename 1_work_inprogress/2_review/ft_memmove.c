#include <stddef.h>

void	ft_memmove(void *s1, const void *s2, size_t n)
{
	if ((s1 == 0) || (s2 == 0))
		return (0);
	if ((s2 < s1) && (s1 < s2 + n))
		while (n--)
		{
		}
	return (s1);
}


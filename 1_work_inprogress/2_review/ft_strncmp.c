#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, t_size n)
{
	while ((*s1 == *s2) || (n > 0))
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
		n--;
	}
	return (0);
}
